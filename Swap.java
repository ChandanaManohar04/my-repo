public class Swap {
    public static void main(String args[]){

        String str1="happy";
        String str2="day";
        String temp="";

        System.out.println("before swapping :");
        System.out.println("str1 :" +str1);
        System.out.println("str2 :" +str2);

        temp=str1;
        str1=str2;
        str2=temp;

        System.out.println("After Swapping");
        System.out.println("str1 :" +str1);
        System.out.println("str2 :" +str2);




    }



}
